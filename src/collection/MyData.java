package collection;

public class MyData {

	private String name;
	private int age;
	private boolean hasKids;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public boolean isHasKids() {
		return hasKids;
	}
	public void setHasKids(boolean hasKids) {
		this.hasKids = hasKids;
	}
}
